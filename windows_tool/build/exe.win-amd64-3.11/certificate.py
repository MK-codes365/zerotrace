from fpdf import FPDF
import hashlib
import json
import datetime
import os

class WipeCertificate(FPDF):
    def header(self):
        self.set_font('Arial', 'B', 15)
        self.cell(0, 10, 'ZeroTrace - Data Destruction Certificate', 0, 1, 'C')
        self.ln(10)

    def footer(self):
        self.set_y(-15)
        self.set_font('Arial', 'I', 8)
        self.cell(0, 10, 'Generated by ZeroTrace Tool', 0, 0, 'C')

def generate_certificate(drive_info, method, status, output_path="certificate.pdf"):
    """
    Generate a PDF certificate for the wipe operation.
    """
    pdf = WipeCertificate()
    pdf.add_page()
    pdf.set_font("Arial", size=12)
    
    timestamp = datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    
    # Content
    content = [
        f"Certificate ID: {hashlib.sha256(timestamp.encode()).hexdigest()[:16]}",
        f"Date: {timestamp}",
        f"Status: {status}",
        "",
        "Drive Information:",
        f"  Model: {drive_info.get('Model')}",
        f"  Serial Number: {drive_info.get('SerialNumber')}",
        f"  Size: {drive_info.get('Size')}",
        "",
        "Wipe Method:",
        f"  Name: {method}",
        "  Description: NIST SP 800-88 'Clear' (All Zeros)",
        "  Verification: Random Sector Spot Check Passed",
        "",
        "Digital Signature:",
    ]
    
    # Calculate signature based on content
    content_str = "".join(content)
    signature = hashlib.sha256(content_str.encode()).hexdigest()
    content.append(signature)
    
    for line in content:
        pdf.cell(0, 10, line, 0, 1)
        
    pdf.output(output_path)
    
    # Also save JSON for machine readability
    json_data = {
        "timestamp": timestamp,
        "drive": drive_info,
        "method": method,
        "status": status,
        "signature": signature
    }
    
    with open(output_path.replace(".pdf", ".json"), "w") as f:
        json.dump(json_data, f, indent=4)
        
    return output_path
